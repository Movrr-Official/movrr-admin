// Helper functions
function makeAvailability(days: string[]) {
  return {
    monday: days.includes("Monday"),
    tuesday: days.includes("Tuesday"),
    wednesday: days.includes("Wednesday"),
    thursday: days.includes("Thursday"),
    friday: days.includes("Friday"),
    saturday: days.includes("Saturday"),
    sunday: days.includes("Sunday"),
  };
}

function makeEvent(
  id: string,
  title: string,
  dateStr: string,
  type: "route" | "training" | "meeting" | "other" = "route"
) {
  return {
    id,
    title,
    date: new Date(dateStr),
    type,
  };
}

function makeActivity(
  message: string,
  time: string,
  type: "delivery" | "route_completed" | "issue" | "system"
) {
  return {
    message,
    time,
    type,
  };
}

